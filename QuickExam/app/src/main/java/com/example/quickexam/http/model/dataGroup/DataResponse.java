package com.example.quickexam.http.model.dataGroup;

public class DataResponse {
    private int code;
    private int msg;

    public void setCode(int code) {
        this.code = code;
    }

    public int getCode() {
        return code;
    }
    public int getMsg() {
        return msg;
    }

    public void setMsg(int msg) {
        this.msg = msg;
    }
}
